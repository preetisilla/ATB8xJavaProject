package Sept.ex_11092024;

public class Lab011 {
    public static void main(String[] args) {
        // numeric type
        // integral - byte, short, int, long, char
        // floating type - float , double
        byte b = 100; // 8 bits, 1 Byte
        short s = 128; // 16 bits, 2 Byte
        int i = 1000; // 32 bits, 4 Byte
        long phone_number = 9876543210l; // 64 bits, 8 Byte

        char grade = 'A'; // ASCII values - int assigned

        float f = 3.14f; // 32 bits, 4 Byte 3.14F is also ok
        double d = 3.1435677; // 64 bits, 8 Byte

        // non primitive
        String name = "Teacher";
    }
}
